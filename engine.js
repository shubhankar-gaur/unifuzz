    generate(input, maxLimit = 100, opts = {}) {
        if (!input) return [];

        let resultsMap = new Map(); 
        
        opts = Object.assign({
            homoglyph: true,
            case: false,
            zwsp: false,
            encoding: 'raw'
        }, opts);

        resultsMap.set(input, { type: 'Original', encodedText: this.encode(input, opts.encoding) });
        
        const chars = Array.from(input);
        let permutations = [''];
        
        for (let i = 0; i < chars.length; i++) {
            let char = chars[i];
            let lowerChar = char.toLowerCase();
            
            let options = new Set([char]);
            
            if (opts.homoglyph && this.homoglyphs[lowerChar] && char.match(/[a-zA-Z0-9]/)) {
                const isUpper = char === char.toUpperCase() && char !== lowerChar;
                this.homoglyphs[lowerChar].forEach(h => {
                    options.add(isUpper ? h.toUpperCase() : h);
                });
            }

            if (opts.case && this.caseAnomalies[char]) {
                this.caseAnomalies[char].forEach(c => options.add(c));
            }

            if (opts.symbols && this.symbols[char]) {
                this.symbols[char].forEach(s => options.add(s));
            }
            
            let nextState = [];
            let optArray = Array.from(options);
            
            for (let perm of permutations) {
                for (let opt of optArray) {
                    if (nextState.length >= maxLimit * 3) continue; 
                    
                    nextState.push(perm + opt);
                    
                    if (opts.zwsp && nextState.length < maxLimit * 3) {
                        this.zwChars.forEach(zw => {
                           nextState.push(perm + opt + zw); 
                        });
                    }
                }
            }
            permutations = nextState;
        }

        permutations.forEach(perm => {
            if (resultsMap.size < maxLimit && !resultsMap.has(perm)) {
                let type = 'Homoglyph';
                if (perm.match(/[\u200B-\u200F\uFEFF]/)) type = 'ZW Insert';
                else if (opts.case && perm !== input && !opts.homoglyph) type = 'Case Anomaly';
                
                resultsMap.set(perm, { type, encodedText: this.encode(perm, opts.encoding) });
            }
        });

        let output = [];
        resultsMap.forEach((data, rawText) => {
            output.push({ rawText, text: data.encodedText, type: data.type });
        });

        return output.slice(0, maxLimit);
    },

    encode(text, format) {
        if (format === 'raw') return text;
        if (format === 'url') return encodeURIComponent(text);
        if (format === 'double_url') return encodeURIComponent(encodeURIComponent(text));
        if (format === 'unicode') {
            return Array.from(text).map(c => {
                if (c.charCodeAt(0) < 128) return c;
                let hex = c.charCodeAt(0).toString(16).padStart(4, '0');
                return '\\u' + hex;
            }).join('');
        }
        if (format === 'html') {
             return Array.from(text).map(c => {
                if (c.charCodeAt(0) < 128) return c;
                return '&#' + c.charCodeAt(0) + ';';
            }).join('');
        }
        return text;
    }
